unsigned int r8a779x_usb3_v3_dlmem_len = sizeof(r8a779x_usb3_v3_dlmem)/sizeof(r8a779x_usb3_v3_dlmem[0]);

int rcar_usb3_dl_fw(uint32_t regs)
{
    int  size;

    int retval = 0, index, j, time, timeout = 10000;
    uint32_t data, val;

    /* check whether firmware is downloaded or not */
    val = in32(regs + RCAR_GEN3_USB3_DL_CTRL);
    if (val & RCAR_GEN3_USB3_DL_CTRL_FW_SUCCESS) {
        return EOK; // firmware has been downloaded
    }

    size = r8a779x_usb3_v3_dlmem_len;

    /* Download R-Car USB3.0 firmware */
    val = in32(regs + RCAR_GEN3_USB3_DL_CTRL);
    val |= RCAR_GEN3_USB3_DL_CTRL_ENABLE;
    out32(regs + RCAR_GEN3_USB3_DL_CTRL, val);

    for (index = 0; index < size; index += 4) {
        /* Avoid reading beyond the end of the buffer */
        for (data = 0, j = 3; j >= 0; j--) {
            if ((j + index) < size)
                data |= r8a779x_usb3_v3_dlmem[index + j] << (8 * j);
        }
        out32(regs + RCAR_GEN3_USB3_FW_DATA0, data);
        val = in32(regs + RCAR_GEN3_USB3_DL_CTRL);
        val |= RCAR_GEN3_USB3_DL_CTRL_FW_SET_DATA0;
        out32(regs + RCAR_GEN3_USB3_DL_CTRL, val);

        for (time = 0; time < timeout; time++) {
            val = in32(regs + RCAR_GEN3_USB3_DL_CTRL);
            if ((val & RCAR_GEN3_USB3_DL_CTRL_FW_SET_DATA0) == 0)
                break;
            usec_delay(1);
        }
        if (time == timeout) {
            retval = -ETIMEDOUT;
            break;
        }
    }

    val = in32(regs + RCAR_GEN3_USB3_DL_CTRL);
    val &= ~RCAR_GEN3_USB3_DL_CTRL_ENABLE;
    out32(regs + RCAR_GEN3_USB3_DL_CTRL, val);

    for (time = 0; time < timeout; time++) {
        val = in32(regs + RCAR_GEN3_USB3_DL_CTRL);
        if (val & RCAR_GEN3_USB3_DL_CTRL_FW_SUCCESS) {
            retval = 0;
            break;
        }
        usec_delay(1);
    }
    if (time == timeout) {
        retval = -ETIMEDOUT;
    }

    return retval;
}

